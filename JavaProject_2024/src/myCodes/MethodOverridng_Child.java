package myCodes;

			/** Method Overriding:
			 * 	RunTime Polymorphism or Static Binding
			 * 	can achieve by Inheritance concept
			 * 	@ovveride annotation will occur in child class
			 * 	super keyword will points out parent method
			 * 
			 * 	Rules:
			 * 	Both Parent Class and Child Class method name should be same and it should be public
			 * 	Static and Final Methods cannot be Overridden
			 * 
			 * 	child method will execute first as we are creating object for child.
			 */

public class MethodOverridng_Child extends MethodOverriding_Parent{
	@Override
	public void parentRP() {
		System.out.println("child RP");
		super.parentRP();
	}
			
	public static void main(String[] args) {
		MethodOverridng_Child mc = new MethodOverridng_Child();
		mc.parentRP();
	}

}
